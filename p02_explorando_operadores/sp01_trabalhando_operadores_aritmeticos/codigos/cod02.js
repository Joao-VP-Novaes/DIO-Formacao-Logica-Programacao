let contador = 1

console.log(contador); // 1

// Mostra e depois executa o incremento
console.log(contador++); // 1
console.log(contador); // 2

// Executa o incremento e depois mostra o valor.
console.log(++contador); // 3

// Mostra e depois executa o decremento.
console.log(contador--); // 3
console.log(contador); // 2

// Executa o decremento e depois mostra o valor.
console.log(--contador); // 1